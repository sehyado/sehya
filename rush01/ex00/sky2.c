void	sky2_1(char *a[], char num[4][4])
{
	int		i;
	int		j;

	i = 1;
	while (i <= 12)
	{
		j = 0;
		if (*a[i] == '1' && *a[i + 4] == '2')
		{
			if (i >= 9 && i <= 12)
			{
				num[i - 9][0] = 4 + 48;
				num[i - 9][3] = 3 + 48;
			}
			if (i >= 1 && i <= 4)
			{
				num[0][i - 1] = 4 + 48;
				num[3][i - 1] = 3 + 48;
			}
		}
		if (i == 4)
			i += 4;
		i++;
	}
}

