#include <unistd.h>

void	zero(int in[4])
{
	int		i;

	i = 0;
	while (i < 4)
	{
		in[i] = -1;
		i++;
	}
}

void	zero_1(int count[4][4])
{
	int		i;
	int		j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			count[i][j] = 0;
			j++;
		}
		i++;
	}
}
