#include <unistd.h>
#include <stdio.h>

void	sky1_1(char *a[], char num[4][4], int i, int j)
{
	while (++i <= 12)
	{
		j = -1;
		if (*a[i] == '1' && *a[i + 4] == '4')
		{
			if (i >= 9 && i <= 12)
			{
				while (++j < 4)
					num[i - 9][j] = 4 - j + 48;
			}
			if (i >= 1 && i <= 4)
			{
				while (++j < 4)
					num[j][i - 1] = 4 - j + 48;
			}
		}
		if (i == 4)
			i += 4;
	}
}

