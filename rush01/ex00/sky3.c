void	sky3(char *a[], char num[4][4])
{
	int		i;
	int		j;

	i = 1;
	while (i <= 16)
	{
		j = 0;
		if (*a[i] == '1')
		{
			if (i >= 1 && i <= 4)
				num[0][i - 1] = 4 + 48;
			if (i >= 5 && i <= 8)
				num[3][i - 5] = 4 + 48;
			if (i >= 9 && i <= 12)
				num[i - 9][0] = 4 + 48;
			if (i >= 13 && i <= 16)
				num[i - 13][3] = 4 + 48;
		}
		i++;
	}
}
