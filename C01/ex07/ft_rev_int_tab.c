#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int q;
	int w;

	q = 0;
	while (q < size / 2)
	{
		w = tab[q];
		tab[q] = tab[size - 1 - q];
		tab[size - 1 - q] = w;
		q++;
	}
}
